//Operador condicional - Ternário
// Podemos utilizar o operador ternário que funciona com o mesmo conceito do if e else, porém, a única diferença é que precisamos devolver um valor após o teste estritamente em uma única linha.

//condição? valor se for verdareiro : valor se for falso

console.log(true ? 'foo' :  'bar') //Retorna 'foo'
console.log(false ? 'foo': 'bar') //retorna 'bar'